// Инициализация кнопки сохранения
function initSaveButton() {
    const saveButton = document.querySelector('.save-recipe-btn-large');
    if (saveButton) {
        saveButton.addEventListener('click', function() {
            const savedRecipes = JSON.parse(localStorage.getItem('savedRecipes')) || [];
            const isCurrentlySaved = savedRecipes.includes(currentRecipeId);
            
            if (isCurrentlySaved) {
                // Удаляем из сохраненных
                const updatedSaved = savedRecipes.filter(id => id !== currentRecipeId);
                localStorage.setItem('savedRecipes', JSON.stringify(updatedSaved));
                saveButton.classList.remove('saved');
                saveButton.innerHTML = '<i class="far fa-bookmark"></i>';
                saveButton.setAttribute('aria-label', 'Сохранить рецепт');
                showNotification('Рецепт удален из сохраненных!');
            } else {
                // Добавляем в сохраненные
                savedRecipes.push(currentRecipeId);
                localStorage.setItem('savedRecipes', JSON.stringify(savedRecipes));
                saveButton.classList.add('saved');
                saveButton.innerHTML = '<i class="fas fa-bookmark"></i>';
                saveButton.setAttribute('aria-label', 'Удалить из сохраненных');
                showNotification('Рецепт добавлен в сохраненные!');
            }
            
            updateSavedCount();
        });
    }
}

// Инициализация взаимодействия с ингредиентами
function initIngredientsInteraction() {
    const ingredients = document.querySelectorAll('.ingredients-list li:not(.ingredient-header)');
    ingredients.forEach(ingredient => {
        ingredient.addEventListener('click', function() {
            this.classList.toggle('crossed-out');
        });
    });
}

// Вспомогательная функция для получения текста сложности
function getDifficultyText(difficulty) {
    const difficultyMap = {
        'easy': 'Легко',
        'medium': 'Средне',
        'hard': 'Сложно'
    };
    return difficultyMap[difficulty] || 'Не указано';
}

// Простая функция для форматирования ингредиентов
function formatIngredients(ingredients) {
    return ingredients.map(ingredient => {
        // Проверяем, является ли строка заголовком
        if (ingredient.trim().endsWith(':')) {
            return `<li class="ingredient-header">${ingredient}</li>`;
        }
        
        // Список common единиц измерения
        const commonUnits = ['г', 'гр', 'кг', 'мл', 'л', 'ст', 'ч.л', 'ст.л', 'шт', 'пучок', 'зубчик', 'щепотка'];
        const commonPhrases = ['по вкусу', 'для подачи', 'для украшения'];
        
        let amount = '';
        let name = ingredient;
        
        // Ищем common фразы
        for (const phrase of commonPhrases) {
            if (ingredient.toLowerCase().includes(phrase)) {
                amount = phrase;
                name = ingredient.replace(phrase, '').trim();
                break;
            }
        }
        
        // Ищем единицы измерения с цифрами
        if (!amount) {
            for (const unit of commonUnits) {
                const regex = new RegExp(`(\\d+\\s*${unit})`, 'i');
                const match = ingredient.match(regex);
                if (match) {
                    amount = match[0];
                    name = ingredient.replace(match[0], '').trim();
                    break;
                }
            }
        }
        
        // Чистим название от лишних символов
        name = name.replace(/^[,\s-]+|[,\s-]+$/g, '');
        
        return `
            <li>
                <span class="ingredient-name">${name}</span>
                <span class="ingredient-amount">${amount}</span>
            </li>
        `;
    }).join('');
}

// Функция для получения штампа в зависимости от категории
function getCategoryStamp(category) {
    const stamps = {
        'salads': 'Салат',
        'soups': 'Супы',
        'main': 'Основное',
        'desserts': 'Десерт',
        'drinks': 'Напиток',
        'preserves': 'Заготовка',
        'seasonal': 'Сезонное'
    };
    return stamps[category] || 'Рецепт';
}

// Функция для получения приборов в зависимости от категории
function getAppliances(category) {
    const appliancesMap = {
        'salads': ['Нож', 'Миска', 'Сковорода'],
        'soups': ['Кастрюля', 'Нож', 'Плита'],
        'main': ['Сковорода', 'Нож', 'Плита'],
        'desserts': ['Духовка', 'Миска', 'Венчик'],
        'drinks': ['Блендер', 'Кувшин', 'Ложка'],
        'preserves': ['Кастрюля', 'Банки', 'Нож'],
        'seasonal': ['Нож', 'Плита', 'Посуда']
    };
    
    const appliances = appliancesMap[category] || ['Нож', 'Плита', 'Посуда'];
    return appliances.map(appliance => `<span class="appliance">${appliance}</span>`).join('');
}

// Функция для обновления счетчика сохраненных рецептов
function updateSavedCount() {
    const savedRecipes = JSON.parse(localStorage.getItem('savedRecipes')) || [];
    const savedCountElement = document.getElementById('saved-count');
    if (savedCountElement) {
        savedCountElement.textContent = savedRecipes.length;
    }
}

// Функция для показа уведомления
function showNotification(message) {
    const notification = document.getElementById('notification');
    if (notification) {
        notification.textContent = message;
        notification.classList.add('show');
        setTimeout(() => {
            notification.classList.remove('show');
        }, 3000);
    }
}